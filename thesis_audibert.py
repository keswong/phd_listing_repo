testing_loss = []
f1_scores = []

for epoch in epochs:
    file_path = f'/content/drive/MyDrive/AIED25/M4_Audibert/AFFECTIVE/results_epoch_{epoch}.pkl'
    data = pd.read_pickle(file_path)  # This is a list of dictionaries
    training_loss.append(data[0]['average training loss'])
